    {
        $request->validate([

            'certificate' => 'required',
            'cfile' => 'required',

        ]);
         
        $certificate=new Certificate;
        $certificate->certificate= $request->input('certificate');
        $certificate->cfile= $request->input('cfile');

        $certificate->save();
        return redirect('/profile');

        

   

